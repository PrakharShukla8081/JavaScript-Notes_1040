// 🔴 Non-Primitive (Reference Types)

// These are objects and can store multiple values.

// 👉 Types:
// Object
// Array
// Function

//by refrence
// let obj1 = { name: "Prakhar" };
// let obj2 = obj1;

// obj2.name = "Rahul";

// console.log(obj1.name); // Rahul

// -------------------------------------------------------------------------------


// | Feature    | Primitive      | Non-Primitive |
// | ---------- | -------------- | ------------- |
// | Stored     | By value       | By reference  |
// | Mutability | Immutable      | Mutable       |
// | Example    | number, string | object, array |
// | Comparison | By value       | By reference  |


// /👉 “Stored by value means a copy of the value is passed, while stored by reference means the memory address is shared between variables.”